#include "main.h"
#include "cmsis_os.h"

TIM_HandleTypeDef htim2;
xSemaphoreHandle semToggleLed;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_TIM2_Init(void);
void StartDefaultTask(void const * argument);
static void MX_NVIC_Init(void);

void TareaLed(void *p);
void ChocarCalesita(void);


int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_NVIC_Init();

  //Creo el semáforo que espera la tarea led
  if(!(semToggleLed = xSemaphoreCreateBinary()))
  {
  	ChocarCalesita();
  }
  HAL_TIM_Base_Start_IT(&htim2);
  xSemaphoreTake(semToggleLed,0);

  if(xTaskCreate(TareaLed,"led",128,NULL,1,NULL)!=pdPASS)
  {
	  ChocarCalesita();
  }

  vTaskStartScheduler();
  for(;;);
}

